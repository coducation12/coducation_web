-- =============================================
-- 🔧 상담문의 INSERT 문제 해결
-- =============================================
-- 일반 사용자도 상담문의를 작성할 수 있도록 정책을 수정합니다.

-- =============================================
-- 1. 기존 INSERT 정책 삭제
-- =============================================
DROP POLICY IF EXISTS "인증된 사용자는 상담문의 작성 가능" ON public.consultations;

-- =============================================
-- 2. 새로운 INSERT 정책 생성
-- =============================================
-- 모든 사용자는 상담문의 작성 가능 (로그인 불필요)
CREATE POLICY "모든 사용자는 상담문의 작성 가능" ON public.consultations
    FOR INSERT WITH CHECK (true);

-- =============================================
-- 3. 완료 메시지
-- =============================================
SELECT '상담문의 작성이 모든 사용자에게 허용되었습니다!' as message;
