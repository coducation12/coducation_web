-- =============================================

-- 모든 사용자는 콘텐츠 조회 가능 (메인페이지용)
CREATE POLICY "모든 사용자는 콘텐츠 조회 가능" ON public.content_management
    FOR SELECT USING (true);

-- =============================================
-- 2. 완료 메시지
-- =============================================
SELECT 'content_management 테이블 공개 조회가 가능해졌습니다!' as message;
