export const timeToIndex = (time: string): number => {
    const [h, m] = time.split(":").map(Number);
    // 12:00 -> 0, 12:15 -> 1, 12:30 -> 2, 12:45 -> 3, 13:00 -> 4 ...
    const idx = (h - 12) * 4 + Math.floor(m / 15);
    return Math.max(0, idx);
};
